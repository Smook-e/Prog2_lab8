    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(feedbackCheck)
        {
            feedbackCheck=false;
            questionIndex++;
        if(questionIndex< questions.size())
        {
            showQuestion(questionIndex);
        }
        else
        {
            int score=quizService.calculateScore(quiz.getQuizId(), answers);
            JOptionPane.showMessageDialog(this, "Quiz Completed! Score:"+score+"/"+questions.size());
            //open the frame that called it 
            this.dispose();
            getWindowAncestor(lessonManagement).setVisible(true);
        }
           return; 
        }
        int selected=-1;
        if(jRadioButton1.isSelected())selected=0;
        else if(jRadioButton2.isSelected())selected=1;
        else if(jRadioButton3.isSelected())selected=2;
        else if(jRadioButton4.isSelected())selected=3;
        if(selected==-1)
        {
            JOptionPane.showMessageDialog(this, "please select a choice.");
            return;
        }
        answers.set(questionIndex, selected);
        Question q=questions.get(questionIndex);
        feedback(selected,q);
        feedbackCheck=true;
        if(questionIndex==questions.size()-1)
        {
            jButton1.setText("Finish Quiz");
        }
        else
        {
            jButton1.setText("Next");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

