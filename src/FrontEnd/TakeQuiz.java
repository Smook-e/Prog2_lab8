    public void openQuiz(String quizId)
    {
        try{
            questions=quizService.takeQuiz(quizId);
        }catch(IllegalArgumentException e){
            JOptionPane.showMessageDialog(this, "Quiz not found");
            return;
        }
        quiz=quizService.getQuizById(quizId);
        answers=new ArrayList<>();
        for(int i=0;i<questions.size();i++)
        {
            answers.add(-1);
        }
        questionIndex=0;
        showQuestion(questionIndex);
    }
    private void showQuestion(int questionIndex)
    {
        if(questionIndex<0||questionIndex>=questions.size())return;
        jTextField2.setBackground(Color.WHITE);
        jTextField3.setBackground(Color.WHITE);
        jTextField4.setBackground(Color.WHITE);
        jTextField5.setBackground(Color.WHITE);
        Question question=questions.get(questionIndex);
        jTextField1.setText(question.getText());
        jTextField2.setText(question.getChoices().get(0));
        jTextField3.setText(question.getChoices().get(1));
        jTextField4.setText(question.getChoices().get(2));
        jTextField5.setText(question.getChoices().get(3));
        buttonGroup1.clearSelection();
        if(questionIndex==questions.size()-1)
        {
            jButton1.setText("Finish Quiz");
        }
        else
        {
            jButton1.setText("Next");
        }
    }
    private void feedback(int selected,Question question)
    {
        jTextField2.setBackground(Color.WHITE);
        jTextField3.setBackground(Color.WHITE);
        jTextField4.setBackground(Color.WHITE);
        jTextField5.setBackground(Color.WHITE);
        List<JRadioButton> buttons=List.of(jRadioButton1,jRadioButton2,jRadioButton3,jRadioButton4);
        List<JTextField> fields=List.of(jTextField2,jTextField3,jTextField4,jTextField5);
        fields.get(question.getCorrectChoice()).setBackground(Color.GREEN);
        if(selected!=question.getCorrectChoice()&&selected>=0)
        {
            fields.get(selected).setBackground(Color.red);
        }
    }

