    private void setupTable() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Student ID");
        model.addColumn("Student Name");
        model.addColumn("Completed Lessons");
        model.addColumn("Average Quiz Score");

        studentsTable.setModel(model);
    }

   private void loadStudentPerformance() {
    DefaultTableModel model = (DefaultTableModel) studentsTable.getModel();
    model.setRowCount(0);

    // Get student IDs enrolled in the course
    List<String> studentIds = instructorManagment.getStudentsInCourse(courseId);

    if (studentIds == null || studentIds.isEmpty()) {
        JOptionPane.showMessageDialog(this, "No students enrolled in this course.");
        return;
    }

    for (String studentId : studentIds) {

        // Convert studentId â†’ Student object
        Student student = instructorManagment.getStudentService().getStudentById(studentId);

        if (student == null) {
            System.out.println("Warning: Student not found: " + studentId);
            continue;
        }

        // Get completed lessons count
        int completedLessons = instructorManagment.getStudentCompletedLessonsCount(studentId, courseId);

        // Get average quiz score
        double avgScore = instructorManagment.getStudentAverageQuizScore(studentId, courseId);

        // Add row to table
        model.addRow(new Object[]{
                student.getUserID(),
                student.getUserName(),
                completedLessons,
                avgScore
        });
    }
}

